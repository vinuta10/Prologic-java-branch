package com.synel.synergy.synergy2416.presentation.view;

public class FPenrollWindow {
	private String badgeNum;
	private int fingerNum;
	private boolean isEnrolled=false;
	
	public String getBadgeNum() {
		return badgeNum;
	}
	public void setBadgeNum(String badgeNum) {
		this.badgeNum = badgeNum;
	}
	public int getFingerNum() {
		return fingerNum;
	}
	public void setFingerNum(int fingerNum) {
		this.fingerNum = fingerNum;
	}
	public boolean isEnrolled() {
		return isEnrolled;
	}
	public void setEnrolled(boolean isEnrolled) {
		this.isEnrolled = isEnrolled;
	}

}
